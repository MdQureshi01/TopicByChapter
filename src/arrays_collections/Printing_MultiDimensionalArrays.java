package arrays_collections;

public class Printing_MultiDimensionalArrays {

	public static void main(String[] args) {
		
		int[] ageSkywalker = new int []{25, 35, 30, 40, 32, 28};           //Index: 0   1   2   3   4   5	
		String[] nameSkywalker = new String []{"Soniah", "Lemon", "Habib"}; //Index: 0 1 2  
			
			
	    System.out.println("Array Value : " + ageSkywalker[1]);
		System.out.println("ageSkywalker Array Length : " + ageSkywalker.length);
		System.out.println("Array Value : " + nameSkywalker[1]);
		System.out.println("nameSkywalker Array Length : " + nameSkywalker.length);

	}

}
