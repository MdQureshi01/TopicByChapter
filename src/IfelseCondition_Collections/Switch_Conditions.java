package IfelseCondition_Collections;

public class Switch_Conditions {

	public static void main(String[] args) {
		
		int day=5;
		
		switch(day) {
		case 01: System.out.println("Sunday"); break;
		case 02: System.out.println("Monday"); break;
		case 03: System.out.println("Tuesday"); break;
		case 04: System.out.println("Wednesday"); break;
		case 05: System.out.println("Thursday"); break;
		case 06: System.out.println("Friday"); break;
		case 07: System.out.println("Saturday"); break;
		default: System.out.println("Not a week"); break;
		    
		}
		}

	}
